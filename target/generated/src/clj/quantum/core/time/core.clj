(defn ^Delay for-days-between
  [date-a date-b f]
  (let [difference-in-days
          (time/in-days (time/interval date-a date-b))
        instants-on-beg-of-days
          (periodic/periodic-seq
            date-a (time/days 1))]
  (for+ [day (take (inc difference-in-days) instants-on-beg-of-days)]
    (f day))))

     
